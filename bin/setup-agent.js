#!/usr/bin/env node
"use strict";
/**
 * bin/setup-agent.js
 * Automated setup script for creating new agents in agents-runtime projects
 * Usage: npm run setup-agent [--name power-agent] [--level 3]
 */

const fs = require("fs");
const path = require("path");
const readline = require("readline");

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout,
});

function question(query) {
  return new Promise((resolve) => rl.question(query, resolve));
}

async function setupAgent() {
  console.log("\n🚀 Agent Setup Wizard\n");
  console.log("=====================================\n");

  // Get agent details
  const agentName =
    process.argv
      .find((arg) => arg.startsWith("--name="))
      ?.split("=")[1] ||
    (await question("Agent name (default: power-agent): ")) ||
    "power-agent";

  const authLevel =
    parseInt(
      process.argv.find((arg) => arg.startsWith("--level="))?.split("=")[1]
    ) ||
    3;

  const agentId = agentName.replace(/\s+/g, "-").toLowerCase();

  console.log(`\n📋 Configuration:`);
  console.log(`   Name: ${agentName}`);
  console.log(`   ID: ${agentId}`);
  console.log(`   Authorization Level: ${authLevel}`);
  console.log(`   Type: Orchestrator Agent`);

  // Create directories
  const dirs = [
    `.agents/skills/${agentId}`,
    `.agents/helpers`,
    `.agents/hooks`,
    `.agents/logs`,
  ];

  console.log("\n📁 Creating directories...");
  for (const dir of dirs) {
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true });
      console.log(`   ✓ ${dir}`);
    }
  }

  // Generate manifest
  const manifest = {
    agent: {
      id: agentId,
      role: "orchestrator",
      authorization_level: authLevel,
      skill_set: [agentId],
      description: `${agentName} — Custom orchestrator agent with full capabilities`,
    },
    metadata: {
      version: "1.0.0",
      created: new Date().toISOString(),
      author: "setup-agent",
    },
  };

  const manifestPath = ".agents/manifest.json";
  fs.writeFileSync(manifestPath, JSON.stringify(manifest, null, 2));
  console.log(`\n✓ Created ${manifestPath}`);

  // Create .gitignore
  const gitignorePath = ".agents/.gitignore";
  if (!fs.existsSync(gitignorePath)) {
    const gitignoreContent = `# Agent runtime (local development only)
*.log
logs/
.memory-store
runs/
*.tmp
.env
.env.local
`;
    fs.writeFileSync(gitignorePath, gitignoreContent);
    console.log(`✓ Created ${gitignorePath}`);
  }

  // Update project .gitignore
  const projectGitignore = ".gitignore";
  let gitignoreContent = fs.readFileSync(projectGitignore, "utf8");
  if (!gitignoreContent.includes(".agents/")) {
    gitignoreContent += "\n# Agent runtime (local development only)\n.agents/\n";
    fs.writeFileSync(projectGitignore, gitignoreContent);
    console.log(`✓ Updated ${projectGitignore}`);
  }

  // Create SETUP.md documentation
  const setupMdPath = ".agents/SETUP.md";
  const setupMdContent = `# Agent Setup Guide

## ${agentName} (${agentId})

**Authorization Level:** ${authLevel}  
**Created:** ${new Date().toISOString().split("T")[0]}

### Directory Structure

\`\`\`
.agents/
├── manifest.json           # Agent configuration
├── skills/
│   └── ${agentId}/         # Main skill handler
│       ├── handler.js
│       └── lib/            # Utility modules
├── helpers/                # Helper utilities
├── hooks/                  # Lifecycle hooks
└── logs/                   # Audit logs (local only)
\`\`\`

### Usage

\`\`\`bash
# Run agent from agents-runtime
npm run agent -- --agent-id ${agentId} --skill ${agentId}

# Or programmatically
const runtime = await createRuntime({ projectRoot: '.' });
const result = await runtime.execute({
  agentId: '${agentId}',
  skillId: '${agentId}',
  input: { operation: 'file:read', params: { path: 'README.md' } }
});
\`\`\`

### Authorization

- **Level:** ${authLevel}
- **Capabilities:** Full (files, code, shell, audit)
- **Restrictions:** Path validation, command whitelist

### Security

- ✓ Input validation (all operations)
- ✓ Path traversal prevention
- ✓ Command execution whitelist
- ✓ Audit logging (all operations)
- ✓ Rate limiting

### Next Steps

1. Customize \`manifest.json\` with your settings
2. Add custom skill logic to \`skills/${agentId}/handler.js\`
3. Implement hooks in \`hooks/\` (optional)
4. Test with \`npm test\`
5. Deploy to production

---

Generated by \`setup-agent\` on ${new Date().toISOString()}
`;

  fs.writeFileSync(setupMdPath, setupMdContent);
  console.log(`✓ Created ${setupMdPath}`);

  console.log(`\n${"=".repeat(37)}`);
  console.log(`\n✅ Agent setup complete!\n`);
  console.log(`📖 Read .agents/SETUP.md for next steps`);
  console.log(`🔐 Remember: .agents/ is in .gitignore (local only)\n`);

  rl.close();
}

setupAgent().catch((err) => {
  console.error("❌ Setup failed:", err.message);
  process.exit(1);
});
