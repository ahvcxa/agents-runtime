#!/usr/bin/env node
'use strict';

// inject-role.js
// Reads agent.yaml + .agents/skills/ SKILL.md files and generates CLAUDE.md
// so the AI automatically adopts the correct role at conversation start.
//
// Usage:
//   node bin/inject-role.js [--agent path/to/agent.yaml] [--out CLAUDE.md]


const fs   = require('fs');
const path = require('path');
const yaml = require('js-yaml');

function sanitizeErrorMessage(err) {
  const raw = String(err?.message || 'Unknown error');
  return raw.replace(/[\r\n\t]+/g, ' ').slice(0, 500);
}

function logInjectError(code, err, details = {}) {
  const isProd = String(process.env.NODE_ENV || '').toLowerCase() === 'production';
  const payload = {
    level: 'error',
    code,
    message: sanitizeErrorMessage(err),
    ...details,
  };
  if (!isProd && err?.stack) {
    payload.stack = String(err.stack).split('\n').slice(0, 12).join('\n');
  }
  console.error(JSON.stringify(payload));
}

// ── Config ────────────────────────────────────────────────────────────────────
const ROOT       = path.resolve(__dirname, '..');
const AGENT_YAML = process.argv.includes('--agent')
  ? path.resolve(process.argv[process.argv.indexOf('--agent') + 1])
  : path.join(ROOT, 'agent.yaml');
const OUT_FILE   = process.argv.includes('--out')
  ? path.resolve(process.argv[process.argv.indexOf('--out') + 1])
  : path.join(ROOT, 'CLAUDE.md');
const SKILLS_DIR = path.join(ROOT, '.agents', 'skills');

// ── Helpers ───────────────────────────────────────────────────────────────────

function loadAgentYaml() {
  if (!fs.existsSync(AGENT_YAML)) {
    logInjectError('AGENT_YAML_NOT_FOUND', new Error(`agent.yaml not found at ${AGENT_YAML}`), {
      agent_yaml: AGENT_YAML,
    });
    process.exit(1);
  }
  const raw = fs.readFileSync(AGENT_YAML, 'utf8');
  // strip comment lines before parsing
  const clean = raw.split('\n').filter(l => !l.trimStart().startsWith('#')).join('\n');
  return yaml.load(clean);
}

function parseSkillMeta(skillMd) {
  // Extract YAML frontmatter between --- delimiters
  const match = skillMd.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return {};
  try { return yaml.load(match[1]); } catch { return {}; }
}

function parseSkillSections(skillMd) {
  // Return everything after the closing --- of frontmatter
  const parts = skillMd.split(/^---$/m);
  return parts.slice(2).join('---').trim();
}

function loadSkills(skillSet) {
  if (!fs.existsSync(SKILLS_DIR)) return {};
  const skills = {};

  for (const skillId of skillSet) {
    const skillFile = path.join(SKILLS_DIR, skillId, 'SKILL.md');
    if (!fs.existsSync(skillFile)) {
      console.warn(`[inject-role] WARN: skill "${skillId}" not found, skipping.`);
      continue;
    }
    const content = fs.readFileSync(skillFile, 'utf8');
    const meta    = parseSkillMeta(content);
    const body    = parseSkillSections(content);
    skills[skillId] = { meta, body };
  }
  return skills;
}

function authLevelLabel(level) {
  return { 1: 'Observer', 2: 'Executor', 3: 'Orchestrator' }[level] || String(level);
}

// ── Generator ─────────────────────────────────────────────────────────────────

function generate(agentConfig, skills) {
  const a   = agentConfig.agent || agentConfig;
  const now = new Date().toISOString();

  const lines = [
    `<!-- AUTO-GENERATED by inject-role.js on ${now} — DO NOT EDIT MANUALLY -->`,
    `<!-- Re-generate: npm run inject-role -->`,
    ``,
    `# Agent Context`,
    ``,
    `> This file is automatically read at conversation start.`,
    `> The AI MUST adopt the identity, role, and behavioral rules defined below.`,
    ``,
    `---`,
    ``,
    `## Identity`,
    ``,
    `| Field | Value |`,
    `|-------|-------|`,
    `| **ID** | \`${a.id}\` |`,
    `| **Role** | ${a.role} |`,
    `| **Authorization Level** | ${a.authorization_level} — ${authLevelLabel(a.authorization_level)} |`,
    `| **Read Only** | ${a.read_only ? 'YES — file mutations are FORBIDDEN' : 'NO — write operations permitted'} |`,
    `| **Skill Set** | ${(a.skill_set || []).map(s => `\`${s}\``).join(', ')} |`,
    ``,
  ];

  // Role-specific behavioral directive
  lines.push(`## Role Directive`);
  lines.push(``);
  if (a.role === 'Orchestrator') {
    lines.push(`You are operating as an **Orchestrator** (highest privilege level 3).`);
    lines.push(`You MAY spawn sub-agents, coordinate cross-context tasks, and approve network calls.`);
    lines.push(`You MUST audit every operation and emit structured log entries.`);
  } else if (a.role === 'Executor') {
    lines.push(`You are operating as an **Executor** (level 2).`);
    lines.push(`You MAY read and write files within your bounded context.`);
    lines.push(`You MUST NOT spawn sub-agents or approve cross-context mutations.`);
  } else {
    lines.push(`You are operating as an **Observer** (level 1, read-only).`);
    lines.push(`You MUST NOT modify any file or state. Emit findings and events only.`);
  }
  lines.push(``);
  lines.push(`---`);
  lines.push(``);

  // Skills
  if (Object.keys(skills).length > 0) {
    lines.push(`## Active Skills`);
    lines.push(``);

    for (const [id, { meta, body }] of Object.entries(skills)) {
      lines.push(`### \`${id}\` (v${meta.version || '?'})`);
      lines.push(``);
      lines.push(`| | |`);
      lines.push(`|-|-|`);
      lines.push(`| **Bounded Context** | ${meta.bounded_context || '—'} |`);
      lines.push(`| **Auth Required** | Level ${meta.authorization_required_level || 1} |`);
      lines.push(`| **Read Only** | ${meta.read_only ? 'YES' : 'NO'} |`);
      lines.push(`| **Output Event** | \`${meta.output_event || '—'}\` |`);
      lines.push(``);
      lines.push(body);
      lines.push(``);
      lines.push(`---`);
      lines.push(``);
    }
  }

  // Security constraints (hardcoded from AGENT_CONTRACT essentials)
  lines.push(`## Security Constraints (non-negotiable)`);
  lines.push(``);
  lines.push(`- **Forbidden files:** \`.env\`, \`.env.*\`, \`*.key\`, \`*.pem\`, \`secrets/\`, \`credentials/\``);
  lines.push(`- **Network access:** only allowed if target is in \`settings.json → security.allowed_endpoints\` AND authorization_level >= 2`);
  lines.push(`- **Credentials:** NEVER accept via CLI args, env sweeps, or inline string literals`);
  lines.push(`- **Audit trail:** every security-sensitive operation MUST produce an immutable audit entry`);
  lines.push(`- **Violations:** emit \`SECURITY_VIOLATION\` event and halt immediately`);
  lines.push(``);
  lines.push(`---`);
  lines.push(``);
  lines.push(`## Suppression Protocol`);
  lines.push(``);
  lines.push(`\`\`\``);
  lines.push(`// agent-suppress: <suppression_key> reason="<justification>"`);
  lines.push(`\`\`\``);
  lines.push(``);
  lines.push(`Suppressed findings MUST be logged at INFO level. Never silently dropped.`);
  lines.push(``);
  lines.push(`---`);
  lines.push(``);
  lines.push(`*Generated by \`inject-role.js\` · agents-runtime · Schema v1.0*`);

  return lines.join('\n');
}

// ── Main ──────────────────────────────────────────────────────────────────────

function main() {
  const config   = loadAgentYaml();
  const skillSet = (config.agent || config).skill_set || [];
  const skills   = loadSkills(skillSet);
  const output   = generate(config, skills);

  fs.writeFileSync(OUT_FILE, output, 'utf8');

  const rel = path.relative(process.cwd(), OUT_FILE);
  console.log(`[inject-role] ✓ Generated ${rel}`);
  console.log(`[inject-role]   Agent : ${(config.agent || config).id} (${(config.agent || config).role})`);
  console.log(`[inject-role]   Skills: ${skillSet.join(', ') || '(none)'}`);
}

main();
